function sumAB(a, b) {
	document.getElementById('result').innerHTML = parseInt(a) + parseInt(b);
	// document.getElementById('resultinput').value = parseInt(a) + parseInt(b);
}
function subAB(a, b) {
	document.getElementById('result').innerHTML = parseInt(a) - parseInt(b);
}
function multiAB(a, b) {
	document.getElementById('result').innerHTML = parseInt(a) * parseInt(b);
}
function divisionAB(a, b) {
	document.getElementById('result').innerHTML = parseInt(a) / parseInt(b);
}